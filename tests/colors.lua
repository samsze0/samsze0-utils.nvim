local color_utils = require("utils.colors")

T.assert_eq(color_utils.rgb_to_hex(255, 255, 255), "#FFFFFF")
T.assert_eq(color_utils.rgb_to_hex(0, 0, 0), "#000000")
T.assert_eq(color_utils.rgb_to_hex(255, 0, 0), "#FF0000")
T.assert_eq(color_utils.rgb_to_hex(0, 255, 0), "#00FF00")
T.assert_eq(color_utils.rgb_to_hex(0, 0, 255), "#0000FF")
T.assert_eq(color_utils.rgb_to_hex(255, 255, 0), "#FFFF00")
T.assert_eq(color_utils.rgb_to_hex(255, 0, 255), "#FF00FF")
T.assert_eq(color_utils.rgb_to_hex(0, 255, 255), "#00FFFF")
T.assert_eq(color_utils.rgb_to_hex(128, 128, 128), "#808080")
T.assert_eq(color_utils.rgb_to_hex(255, 128, 0), "#FF8000")
T.assert_eq(color_utils.rgb_to_hex(128, 255, 0), "#80FF00")
T.assert_eq(color_utils.rgb_to_hex(0, 255, 128), "#00FF80")
T.assert_eq(color_utils.rgb_to_hex(0, 128, 255), "#0080FF")
T.assert_eq(color_utils.rgb_to_hex(128, 0, 255), "#8000FF")
T.assert_eq(color_utils.rgb_to_hex(255, 0, 128), "#FF0080")
T.assert_eq(color_utils.rgb_to_hex(128, 255, 255), "#80FFFF")
T.assert_eq(color_utils.rgb_to_hex(255, 128, 255), "#FF80FF")
T.assert_eq(color_utils.rgb_to_hex(255, 255, 128), "#FFFF80")
T.assert_eq(color_utils.rgb_to_hex(128, 128, 255), "#8080FF")
